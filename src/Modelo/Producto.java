package Modelo;

public class Producto {
	private String nombre;
	private double precio;
	private double cantidad;
	private int id;
	
	
	public Producto(String nombre,double precio,double cantidad,int id) {
		this.nombre=nombre;
		this.precio=precio;
		this.cantidad=cantidad;
		this.id=id;
	}
	
	
}
